// Order n time complexity program example
let arr=[1,2,3,4,5,6,7,22,41,5]

function prinAllElemetOfArr(arr){
    for(let i=0;i<arr.length;i++){
        console.log(`Element of array is ${arr[i]}`)
    }
} 

prinAllElemetOfArr(arr)

// time complexity is Big O(n) 
// also called Linear time algorithm